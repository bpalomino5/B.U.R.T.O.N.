import socket

TCP_IP = '192.168.1.137'
TCP_PORT = 9999
BUFFER_SIZE = 80
lightON = b'\x00\x00\x00\x2a\xd0\xf2\x81\xf8\x8b\xff\x9a\xf7\xd5\xef\x94\xb6\xc5\xa0\xd4\x8b\xf9\x9c\xf0\x91\xe8\xb7\xc4\xb0\xd1\xa5\xc0\xe2\xd8\xa3\x81\xf2\x86\xe7\x93\xf6\xd4\xee\xdf\xa2\xdf\xa2' # LIGHT ON COMMAND
lightOFF = b'\x00\x00\x00\x2a\xd0\xf2\x81\xf8\x8b\xff\x9a\xf7\xd5\xef\x94\xb6\xc5\xa0\xd4\x8b\xf9\x9c\xf0\x91\xe8\xb7\xc4\xb0\xd1\xa5\xc0\xe2\xd8\xa3\x81\xf2\x86\xe7\x93\xf6\xd4\xee\xde\xa3\xde\xa3' # LIGHT OFF COMMAND


def toggleSmartPlug(value=0):
	s = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
	s.connect((TCP_IP, TCP_PORT))
	if value==1:
		s.send(lightON)
	elif value==0:
		s.send(lightOFF)
	data = s.recv(BUFFER_SIZE)
	# print(data)
	s.close()